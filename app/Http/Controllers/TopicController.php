    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $validated = $request->validate([
            'title' => 'required',
            'info' => '',
        ]);

        $topic = new Topic();
        $topic['title'] = $validated['title'];
        $topic['info'] = $validated['info'];
        $topic->save();

        return redirect('/topics');
    }

